void UMenu::HostButtonClicked()
{
	HostButton->SetIsEnabled(false);
	if (MultiplayerSessionsSubsystem)//若子系统存在
	{
		MultiplayerSessionsSubsystem->CreateSession(NumPublicConnections, MatchType);//创建会话，参数是从MenuSeup传进来的

	}
}

void UMenu::JoinButtonClicked()
{
	JoinButton->SetIsEnabled(false);
	if (MultiplayerSessionsSubsystem)
	{
		MultiplayerSessionsSubsystem->FindSessions(10000);
	}
}

void UMenu::MenuTearDown()
{
	RemoveFromParent();
	UWorld* World = GetWorld();
	if (World)
	{
		APlayerController* PlayerController = World->GetFirstPlayerController();
		if (PlayerController)
		{
			FInputModeGameOnly InputModeData;//注意这里是game only,不是ui only
			PlayerController->SetInputMode(InputModeData);//设置输入模式为 game only
			PlayerController->SetShowMouseCursor(false);
		}
	}
}
